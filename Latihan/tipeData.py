# integer ---> menyatakan bilangan bulat
a = 4
print('Nilai a : ', a , 'tipe data : ', type(a))

# float ---> menyatakan bilangan desimal
b = 3.14
print('Nilai b : ',  b , 'tipe data : ', type(b))

# string --> menyatakan karakter/kalimat

c = 'Hello Ayub'
print('Nilai c: ',  c , 'tipe data : ', type(c))

# boolean --> menyatakan True/False

d = True
e = False
print('Nilai d: ',  d , 'tipe data : ', type(d))
print('Nilai e: ',  e , 'tipe data : ', type(e))