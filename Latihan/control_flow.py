## pernyataan if --> menguji 1 kondisi/pernyataan

print('\n -- Pernyataan if --')
angka_1 = 1
if angka_1== 1 :
    print('angka sesuai')

# pernyataan if else --> menguji 2 kondisi/pernyataan
print('\n -- Pernyataan if else --')
angka_2 = 8
if angka_2 == 9 :
    print('angka tidak sesuai')
else :
    print('angka salah')

# pernyataan if elif else --> menguji lebih dari 2 kondisi/pernyataan
print('\n -- Pernyataan if elif else --')
angka_3 = 5
if angka_3 == 6 :
    print('tidak sesuai')
elif angka_3 == 5 :
    print('angka sesuai')
else :
    print('angka yang dimasukkan salah')